def find_waypoint(car_x, car_y):
	# given current x, y, find the most pertinent waypoint (which allows us to constrain the system)
	waypointsByDistance = []
	index = 0
	for waypoint in waypoints:
		distance = dist(waypoint[0], waypoint[1], car_x, car_y)
		waypointsByDistance.append([waypoint[0], waypoint[1], distance])
		index = index + 1
	waypointsByDistance.sort(key = lambda x: x[2])
	
	return waypointsByDistance[:75]

def handle_waypoint(car_x, car_y, car_psi):
	transformed_waypoints_x = []
	transformed_waypoints_y = []
	best_waypoints = find_waypoint(car_x, car_y)
	print(best_waypoints)
	for waypoint in best_waypoints:
		diffx = waypoint[0] - car_x
		diffy = waypoint[1] - car_y
		transformed_waypoints_x.append(diffx * cos(car_psi) + diffy * sin(car_psi))
		transformed_waypoints_y.append(diffy * cos(car_psi) - diffx * sin(car_psi))

	coeff = np.polyfit(transformed_waypoints_x, transformed_waypoints_y, 3)
	#print("coeffs=")
	#print(coeff)
	#print("transformed_waypoints")
	#print(transformed_waypoints_x)
	#print(transformed_waypoints_y)

	return coeff

def perform_mpc(x_init, y_init, psi_init, v_init):
    # start mpc
	mpc_time = 3
	mpc_samples = 30

	Lf = 8.5 * 1.21

	m = ConcreteModel()
	m.t = ContinuousSet(bounds=(0, mpc_time))

	m.x = Var(m.t)
	m.y = Var(m.t)
	m.psi = Var(m.t)
	m.v = Var(m.t)
	m.cte = Var(m.t)
	m.centripetal_accel = Var(m.t)

	m.x_dot = DerivativeVar(m.x)
	m.y_dot = DerivativeVar(m.y)
	m.psi_dot = DerivativeVar(m.psi)
	m.v_dot = DerivativeVar(m.v)

	m.delta = Var(m.t)
	m.throttle = Var(m.t)
	m.delta_dot = DerivativeVar(m.delta, withrespectto=m.t)
	m.throttle_dot = DerivativeVar(m.throttle, withrespectto=m.t)

	coeffs = handle_waypoint(x_init, y_init, psi_init)
	m.cte_eq = Constraint(m.t, rule=lambda m, t: 
		m.cte[t] == (coeffs[3] + coeffs[2] * m.x[t] + coeffs[1] * m.x[t]**2 + coeffs[0] * m.x[t]**3) - m.y[t]
	)
	
	m.ode_x = Constraint(m.t, rule=lambda m, t: m.x_dot[t] == m.v[t]*cos(m.psi[t]))
	m.ode_y = Constraint(m.t, rule=lambda m, t: m.y_dot[t] == m.v[t]*sin(m.psi[t]))
	m.ode_psi = Constraint(m.t, rule=lambda m, t: m.psi_dot[t] == (m.v[t] * tan(m.delta[t])) / Lf)
	m.ode_v = Constraint(m.t, rule=lambda m, t: m.v[t] == m.throttle[t])

	m.delta_constr1 = Constraint(m.t, rule=lambda m, t: m.delta[t] <= math.radians(50))
	m.delta_constr2 = Constraint(m.t, rule=lambda m, t: m.delta[t] >= -math.radians(50))
	m.throttle_constr1 = Constraint(m.t, rule=lambda m, t: m.throttle[t] <= 180)
	m.throttle_constr2 = Constraint(m.t, rule=lambda m, t: m.throttle[t] >= -180)

	m.delta_constr3 = Constraint(m.t, rule=lambda m, t: m.delta_dot[t] <= math.radians(45))
	m.delta_constr4 = Constraint(m.t, rule=lambda m, t: m.delta_dot[t] >= -math.radians(45))
	m.throttle_constr3 = Constraint(m.t, rule=lambda m, t: m.throttle_dot[t] <= 100)
	m.throttle_constr4 = Constraint(m.t, rule=lambda m, t: m.throttle_dot[t] >= -200)

	m.cte_constr = Constraint(m.t, rule=lambda m, t: m.cte[t] <= 30)

	m.centripetal_accel_eq = Constraint(m.t, rule=lambda m, t: m.centripetal_accel[t] == (m.v[t] ** 2) / (Lf / (0.00001 + tan(m.delta[t]))))
	m.centripetal_accel_constr_1 = Constraint(m.t, rule=lambda m, t: m.centripetal_accel[t] <= 100)
	m.centripetal_accel_constr_2 = Constraint(m.t, rule=lambda m, t: m.centripetal_accel[t] >= -100)

	m.pc = ConstraintList()
	m.pc.add(m.x[0]==0)
	m.pc.add(m.y[0]==0)
	m.pc.add(m.psi[0]==0)
	m.pc.add(m.v[0]==v_init)


	m.path_x = Var(m.t)
	m.path_y = Var(m.t)
	#m.path_pick = Var(m.t)
	#m.path_pick_distance = Var(m.t)
	#m.path_pick_distance_eq = Constraint(m.t, rule=lambda m, t: m.x[t] - m.path_x[0])

	def cost_function(m, t):
		return 0.004*(m.v[t]-180)**2 + 0.025*(m.cte[t])**2 + 50.0*(m.delta_dot[t])**2 + 5.0*(m.delta[t])**2 + 0.001*(m.throttle_dot[t])**2

	m.integral = Integral(m.t, wrt=m.t, rule=cost_function)
	m.obj = Objective(expr=m.integral)

	TransformationFactory('dae.finite_difference').apply_to(m, wrt=m.t, nfe=mpc_samples)
	SolverFactory('ipopt').solve(m)

	delta_res = np.array([m.delta[t]() for t in m.t]).tolist()
	throttle_res = np.array([m.throttle[t]() for t in m.t]).tolist()

	t_res = np.array([t for t in m.t]).tolist()

	x_res = np.array([m.x[t]() for t in m.t]).tolist()
	y_res = np.array([m.y[t]() for t in m.t]).tolist()
	psi_res = np.array([m.psi[t]() for t in m.t]).tolist()
	v_res = np.array([m.v[t]() for t in m.t]).tolist()

	cte_res = np.array([m.cte[t]() for t in m.t]).tolist()
	print(cte_res[0])

	use_graph = 1

	if (use_graph == 1):
		conn.send(json.dumps([t_res, x_res, y_res, cte_res, delta_res, v_res]))

	return [throttle_res, delta_res]

